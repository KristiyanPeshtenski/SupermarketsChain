  INTO   :new.id
  FROM   dual;
END;

CREATE TABLE PRODUCTS (
	ID NUMBER(8) PRIMARY KEY NOT NULL,
	VENDORID NUMBER(8) NOT NULL,
	NAME VARCHAR2(50) NOT NULL,
	MEASUREID NUMBER(8) NOT NULL,
	PRICE NUMBER(8) NOT NULL,
  CONSTRAINT FK_PRODUCTS_VENDOR FOREIGN KEY (VENDORID) REFERENCES VENDORS(ID),
  CONSTRAINT FK_PRODUCTS_MEASURE FOREIGN KEY (MEASUREID) REFERENCES MEASURES(ID)
);

CREATE SEQUENCE PRODUCTS_SEQ;

CREATE OR REPLACE TRIGGER PRODUCTS_TRI 
BEFORE INSERT ON PRODUCTS 
FOR EACH ROW
BEGIN
  SELECT PRODUCTS_SEQ.NEXTVAL
  INTO   :new.id
  FROM   dual;
END;

