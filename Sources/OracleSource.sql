CREATE TABLE VENDORS (
	ID NUMBER(8) PRIMARY KEY NOT NULL,
	NAME VARCHAR2(50) NOT NULL
);

CREATE SEQUENCE VENDORS_SEQ;

CREATE OR REPLACE TRIGGER VENDORS_TRI 
BEFORE INSERT ON VENDORS 
FOR EACH ROW
BEGIN
  SELECT VENDORS_SEQ.NEXTVAL
  INTO   :new.id
  FROM   dual;
END;

